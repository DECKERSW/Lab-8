def show_info(students):
    if not students:
        print("Інформація про студентів відсутня.")
    else:
        print("------------------------------------------------------------------------------\nІнформація про студентів:")
        for student, info in students.items():
            print(f"Ім'я студента: {student}")
            print(f"Група: {info['Group']}")
            print(f"Курс: {info['Course']}")
            print("Предмети та оцінки:")
            for subject, grade in info['Subjects'].items():
                print(f"{subject}: {grade}")
            print("------------------------------------------------------------------------------\n")

